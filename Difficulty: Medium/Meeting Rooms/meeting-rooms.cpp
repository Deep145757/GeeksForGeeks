class Solution {
  public:
    bool canAttend(vector<vector<int>> &arr) {
        // Your Code Here
        set<int>st;
        for(int i=0; i<arr.size(); i++){
            for(int j=arr[i][0]; j<arr[i][1]; j++){
                int n = st.size();
                st.insert(j);
                if(n==st.size())
                    return 0;
            }
        }
        return 1;
    }
};

