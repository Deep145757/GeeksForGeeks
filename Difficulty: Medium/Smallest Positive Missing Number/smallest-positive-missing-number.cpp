class Solution {
  public:
    // Function to find the smallest positive number missing from the array.
    int missingNumber(vector<int> &arr) {
        // Your code here
        set<int> st;
        for(int i : arr){
            if(i>0)
                st.insert(i);
        }
        int i=1, n=st.size();
        while(i){
            st.insert(i);
            if(n!=st.size())
                return i;
            i++;
        }
        return INT_MAX;
    }
};

