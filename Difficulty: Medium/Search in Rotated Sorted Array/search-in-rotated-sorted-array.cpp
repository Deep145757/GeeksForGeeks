class Solution {
  public:
    int search(vector<int>& arr, int key) {
        // complete the function here
        bool issmall=0;
        int n = arr.size();
        for(int i=0; i<n; i++){
            if(key == arr[i])
                return i;
            else if(key  > arr[i])
                issmall = 1;
            else if(key > arr[i]){
                if(issmall)
                    return -1;
            }
        }
        return -1;
    }
};

