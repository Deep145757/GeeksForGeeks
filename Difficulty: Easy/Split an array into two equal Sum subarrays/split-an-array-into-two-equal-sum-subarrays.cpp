class Solution {
  public:
    bool canSplit(vector<int>& arr) {
        // code here
        long long int ttl=0;
        for(auto i : arr){
            ttl += i;
        }
        long long int count=0;
        for(int i=0; i<arr.size(); i++){
            count += arr[i];
            ttl -= arr[i];
            if(ttl == count)
                return 1;
        }
        return 0;
    }
};


