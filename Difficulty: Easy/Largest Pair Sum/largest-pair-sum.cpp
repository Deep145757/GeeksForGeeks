class Solution {
  public:
    int pairsum(vector<int> &arr) {
        // code here
        int l1=0, l2=0;
        for(int i : arr){
            if(i >= l1){
                l2 = l1;
                l1 = i;
            }
            else if(i > l2)
                l2 = i;
        }
        return l1 + l2;
    }
};


