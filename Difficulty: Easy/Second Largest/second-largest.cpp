class Solution {
  public:
    // Function returns the second
    // largest elements
    int getSecondLargest(vector<int> &arr) {
        // Code Here
        int max=-1, max2=-1;
        for(int i:arr){
            if(i > max){
                max2 = max;
                max = i;
            }
            else if(i > max2 && i<max)
                max2 = i;
        }
        return max2;
    }
};

