class Solution {
  public:
    int maximumProfit(vector<int> &prices) {
        // code here
        int currMin = INT_MAX;
        int maxDiff = 0;
        for(int i : prices){
            if(currMin > i)
                currMin = i;
            else{
                maxDiff = max(maxDiff, i-currMin);
            }
        }
        return maxDiff;
    }
};


